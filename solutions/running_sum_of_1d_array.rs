///
/// Link: https://leetcode.com/problems/running-sum-of-1d-array
///

// ============================================================================

struct Solution {
}

impl Solution {
    pub fn running_sum(nums: Vec<i32>) -> Vec<i32> {
        let mut ans: Vec<i32> = vec![nums[0]];
        
        for i in 1..nums.len() {
            ans.push(nums[i] + ans[ans.len()-1]);
        }
        ans
    }
}

