#[test]
fn test_2() {
    assert_eq!(Solution::running_sum(vec![1, 1, 1, 1, 1]), vec![1, 2, 3, 4, 5]);
}

// ============================================================================

#[test]
fn test_3() {
    assert_eq!(Solution::running_sum(
            vec![3, 1, 2, 10, 1]), vec![3, 4, 6, 16, 17]);
}

